int	ft_atoi(const char *str)
{
	int					j;
	unsigned long int	k;

	j = 1;
	k = 0;
	while ((*str <= 13 && *str >= 9) || *str == ' ')
		str++;
	if (*str == '-')
		j *= -1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str <= '9' && *str >= '0')
	{
		k = k * 10 + *str - 48;
		str++;
	}
	if (k >= 9223372036854775807 && j > 0)
		return (-1);
	if (k >= 9223372036854775807 && j < 0)
		return (0);
	return (k * j);
}

int main()
{
	printf("%d\n" , ft_atoi("9223372036854775806"));
	printf("%d" , atoi("9223372036854775806"));
}
